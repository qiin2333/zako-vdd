// Returns a fresh copy of the EDID bytes for the given profile.
// Auto leaks through as Modern; callers should resolve Auto themselves
// (see DriverEntry / DetectAutoEdidProfile in Driver.cpp).
inline std::vector<BYTE> GetDefaultEdid(Profile profile) {
    const BYTE* src = (profile == Profile::Legacy)
        ? DEFAULT_EDID_DATA_LEGACY
        : DEFAULT_EDID_DATA_MODERN;
    return std::vector<BYTE>(src, src + 256);
}

// Parse a string spelling of the profile (case-insensitive).
// Empty / unknown -> Auto.
inline Profile ProfileFromString(const std::wstring& s) {
    std::wstring lower;
    lower.reserve(s.size());
    for (wchar_t c : s) {
        lower.push_back((c >= L'A' && c <= L'Z') ? (wchar_t)(c - L'A' + L'a') : c);
    }
    if (lower == L"legacy" || lower == L"win10" || lower == L"1.3") return Profile::Legacy;
    if (lower == L"modern" || lower == L"win11" || lower == L"1.4") return Profile::Modern;
    return Profile::Auto;
}

inline const wchar_t* ProfileToString(Profile p) {
    switch (p) {
        case Profile::Legacy: return L"Legacy";
        case Profile::Modern: return L"Modern";
        case Profile::Auto:   return L"Auto";
    }
    return L"Auto";
}

